SELECT U.USERS_ID, 
       U.LOGIN_NAME, 
		 U.DISPLAY_NAME, 
		 U.PRIMARY_IP, 
		 U.PRIMARY_HOSTNAME_ID,
		 U.PRIMARY_MEMBER_GROUP_ID, 
		 U.DATE_REGISTERED,
		 U.EMAIL_ADDRESS,
		 U.LAST_LOGIN,
		 U.PERSONAL_TEXT,
		 U.GENDER_ID,
		 U.BIRTH_DATE,
		 U.USER_TITLE,
		 U.HIDE_EMAIL_FLAG,
		 U.HIDE_AGE_FLAG,
		 U.HIDE_GENDER_FLAG,
		 U.HIDE_BIRTH_DATE_FLAG,
		 U.TIME_OFFSET,
		 A.AVATAR_FILENAME,
		 A.AVATAR_URL,
		 A.AVATAR_GALLERY_ID,
		 A.AVATAR_ID,
		 A.AVATAR_TYPE_ID,
		 T.TIME_ZONE,
		 U.SIGNATURE,
		 U.NNID,
		 U.SKYPE,
		 U.STEAM,
		 U.GTALK,
		 U.XBOX_LIVE,
		 U.PSN
FROM users U
LEFT JOIN AVATAR A ON A.AVATAR_ID = U.AVATAR_ID
JOIN LKUP_MEMBER_GROUP G ON U.PRIMARY_MEMBER_GROUP_ID = G.MEMBER_GROUP_ID
JOIN LKUP_TIMEZONES T ON T.LKUP_TIMEZONES_ID = U.TIME_OFFSET